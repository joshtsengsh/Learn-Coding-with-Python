data_values = ["Amanda", "Brenda", "Carol", "Dhelia", "Evelyn", "Fiona", "Geraldine", "Hilda", "Ingrid"]

#Print the value at index 0
print("At index 0:")
print(data_values[0])

#Print value at index -1
print("At index -1:")
print(data_values[-1])

#Print value at index 5
print("At index 5:")
print(data_values[5])

