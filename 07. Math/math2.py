#This file shows you some concepts related to performing math calculations in Python using float values

#Note that num1 is a float value, and num2 is an int value
num1 = 5.0
num2 = 10

#All calculations involving float values will result in a float value being produced

print("Addition:")
result = num1 + num2
print(result)
print(type(result))

print("---")

print("Subtraction:")
result = num1 - num2
print(result)
print(type(result))

print("---")

print("Multiplication:")
result = num1 * num2
print(result)
print(type(result))

print("---")


print("Division:")
result = num1 / num2
print(result)
print(type(result))
print("---")
result = num2 / num1
print(result)
print(type(result))
