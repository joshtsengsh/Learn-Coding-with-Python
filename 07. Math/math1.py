#This file shows you some concepts related to performing math calculations in Python

num1 = 5
num2 = 10

#All additions and subtractions of int values produce int values

print("Addition:")
result = num1 + num2
print(result)
print(type(result))

print("---")

print("Subtraction:")
result = num1 - num2
print(result)
print(type(result))

print("---")

#If you multiply an int value with an int value, it will be an int value

print("Multiplication:")
result = num1 * num2
print(result)
print(type(result))

print("---")

#All division, however, produces float (decimal) values

print("Division:")
result = num1 / num2
print(result)
print(type(result))
print("---")
result = num2 / num1
print(result)
print(type(result))
