num1 = 5
num2 = 8

#Using double slash to get a quotient in division
print(num2//num1)
print("---")

#Using the percentage sign to get a remainder in division
print(num2%num1)
print("---")

#Converting a float to int using int() removes the decimal place
#Note that this doesn't perform any rounding; you simply chop off the decimal portions
print(num2/num1)
print(int(8/5))

